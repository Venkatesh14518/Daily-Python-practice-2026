# Subscripting
print("Hello"[0])  
# "Hello" is a string. [0] means “give me the character at index 0”.
# Python starts counting from 0, so this prints the first letter: H

# String
print("123" + "345")  
# Both "123" and "345" are strings (text).
# The + operator joins (concatenates) them.
# Result is "123345", not a mathematical addition.

# Integer = Whole number
print(123 + 345)  
# 123 and 345 are integers (whole numbers).
# The + operator performs real arithmetic.
# Result is 468.

# Large Integers
print(123_456_789)  
# Underscores are allowed in numbers for readability.
# Python ignores the underscores completely.
# This prints the number: 123456789.

# Float = Floating Point Number
print(3.14159)  
# A float is a number with a decimal point.
# Used for precise or fractional values.
# This prints 3.14159.

# Boolean
print(True)  
# True is a Boolean value.
# It represents a logical “yes” or “on”.

print(False)  
# False is also a Boolean value.
# It represents a logical “no” or “off”.
